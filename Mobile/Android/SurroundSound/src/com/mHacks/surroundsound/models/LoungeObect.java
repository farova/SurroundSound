public class LoungeObect {
	
	private String loungeName = null;
	
	private String loungePlaying = null;
	
	private boolean loungeLocked = false;
	
	private String loungeId = null;
	
	private String submittedPassword = null;

	public String getLoungeName() {
		return loungeName;
	}

	public void setLoungeName(String loungeName) {
		this.loungeName = loungeName;
	}

	public String getLoungePlaying() {
		return loungePlaying;
	}

	public void setLoungePlaying(String loungePlaying) {
		this.loungePlaying = loungePlaying;
	}

	public boolean isLoungeLocked() {
		return loungeLocked;
	}

	public void setLoungeLocked(boolean loungeLocked) {
		this.loungeLocked = loungeLocked;
	}

	public String getSubmittedPassword() {
		return submittedPassword;
	}

	public void setSubmittedPassword(String submittedPassword) {
		this.submittedPassword = submittedPassword;
	}

	public String getLoungeId() {
		return loungeId;
	}

	public void setLoungeId(String loungeId) {
		this.loungeId = loungeId;
	}

	
	

}
